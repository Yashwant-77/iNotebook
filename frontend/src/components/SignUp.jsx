import React, { useState } from "react";
import { useNavigate } from "react-router-dom";

function SignUp({ showAlert }) {
  const [crendentials, setCrendentials] = useState({
    name: "",
    email: "",
    password: "",
    cpassword: "",
  });
  const navigate = useNavigate();
  const handleSubmit = async (e) => {
    e.preventDefault();
    if (crendentials.password !== crendentials.cpassword) {
      showAlert("Password Confirmation failed", "danger");
      setCrendentials({ ...crendentials, password: "", cpassword: "" });
      return;
    }
    const { name, email, password } = crendentials;
    const response = await fetch("https://inotebook-2-748t.onrender.com/api/auth/createuser", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ name, email, password }),
    });
    const json = await response.json();
    console.log(json);
    if (json.success) {
      localStorage.setItem("token", json.authtoken);
      navigate("/");
      showAlert("Account Created Successfully", "success");
    } else {
      showAlert("Failed to Create Signup", "danger");
    }
  };
  const onChange = (e) => {
    setCrendentials({ ...crendentials, [e.target.name]: e.target.value });
  };
  return (
    <div>
      <form onSubmit={handleSubmit}>
        <div className="mb-3">
          <label htmlFor="name" className="form-label">
            Name
          </label>
          <input
            type="text"
            className="form-control"
            id="name"
            name="name"
            aria-describedby="emailHelp"
            required
            onChange={onChange}
            value={crendentials.name}
          />
          <label htmlFor="email" className="form-label">
            Email address
          </label>
          <input
            type="email"
            className="form-control"
            id="email"
            aria-describedby="emailHelp"
            required
            onChange={onChange}
            value={crendentials.email}
            name="email"
          />
          <div id="emailHelp" className="form-text">
            We'll never share your email with anyone else.
          </div>
        </div>
        <div className="mb-3">
          <label htmlFor="password" className="form-label">
            Password
          </label>
          <input
            type="password"
            className="form-control"
            id="password"
            name="password"
            required
            minLength={5}
            onChange={onChange}
            value={crendentials.password}
          />
        </div>
        <div className="mb-3">
          <label htmlFor="cpassword" className="form-label">
            Confirm Password
          </label>
          <input
            type="password"
            className="form-control"
            id="cpassword"
            name="cpassword"
            required
            minLength={5}
            onChange={onChange}
            value={crendentials.cpassword}
          />
        </div>
        <button type="submit" className="btn btn-primary">
          Submit
        </button>
      </form>
    </div>
  );
}

export default SignUp;
